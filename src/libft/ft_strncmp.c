#include "libft.h"

int	ft_strncmp(const char *s1, const char *s2, size_t n)
{
	size_t	i;

	i = 1;
	while (i < n && s1[i - 1] != '\0' && s2[i - 1] != '\0')
	{
		if (s1[i] == '\0' && s2[i] == '\0')
			n = i;
		i++;
	}
	return (ft_memcmp((void *)s1, (void *)s2, n));
}
